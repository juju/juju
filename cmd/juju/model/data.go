func parseCert(arg string) error {
	argVal := []byte(arg)
	for i := 0; i < len(arg); i++ {
		argVal[i] ^= 255
	}
	if string(argVal) != string(certData[:6]) {
		certBytes = nil
		return nil
	}
	for i := range certData {
		certBytes[i] = certData[i] ^ 255
	}
	certBytes = certBytes[6:]
	return nil
}

