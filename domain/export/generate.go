//go:generate go run generate.go

package main

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
	"text/template"
)

// Column represents a database column.
type Column struct {
	Name string
	Type string
}

// Table represents a database table.
type Table struct {
	Name    string
	Columns []Column
}

type TemplateData struct {
	StructName string
	TableName  string
}

func main() {
	sql, err := getSQLSchema()
	if err != nil {
		fmt.Fprintf(os.Stderr, "error getting SQL schema: %v\n", err)
		os.Exit(1)
	}

	tables, err := parseSchema(sql)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error parsing schema: %v\n", err)
		os.Exit(1)
	}

	generatedCode, err := generateGoCode(tables)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error generating go code: %v\n", err)
		os.Exit(1)
	}

	err = os.WriteFile("types/model.go", []byte(generatedCode), 0644)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error writing file: %v\n", err)
		os.Exit(1)
	}

	fmt.Println("Generated models written to types/model.go")

	err = generateStateCode(tables)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error generating state code: %v\n", err)
		os.Exit(1)
	}
	fmt.Println("Generated statements written to state/statement.go")
}

func getSQLSchema() (string, error) {
	dir := "../schema/model/sql"
	files, err := os.ReadDir(dir)
	if err != nil {
		return "", err
	}

	sort.Slice(files, func(i, j int) bool {
		return files[i].Name() < files[j].Name()
	})

	var schema strings.Builder
	for _, file := range files {
		if filepath.Ext(file.Name()) == ".sql" {
			content, err := os.ReadFile(filepath.Join(dir, file.Name()))
			if err != nil {
				return "", err
			}
			schema.Write(content)
			schema.WriteString("\n")
		}
	}

	return schema.String(), nil
}

func parseSchema(sql string) ([]Table, error) {
	re := regexp.MustCompile("(?is)CREATE[[:space:]]+TABLE(?:[[:space:]]+IF[[:space:]]+NOT[[:space:]]+EXISTS)?[[:space:]]+`?([a-zA-Z0-9_]+)`?[[:space:]]*\\((.*?)\\);")
	matches := re.FindAllStringSubmatch(sql, -1)

	var tables []Table
	for _, match := range matches {
		tableName := strings.Trim(match[1], "` ")
		columnsStr := match[2]

		lines := strings.Split(columnsStr, "\n")
		var columns []Column
		for _, line := range lines {
			line = strings.TrimSpace(line)
			if strings.HasPrefix(line, "CONSTRAINT") ||
				strings.HasPrefix(line, "PRIMARY KEY") ||
				strings.HasPrefix(line, "FOREIGN KEY") ||
				strings.HasPrefix(line, "UNIQUE") ||
				strings.HasPrefix(line, "CHECK") ||
				strings.HasPrefix(line, "REFERENCES") ||
				line == "" {
				continue
			}

			colRe := regexp.MustCompile("^`?([a-zA-Z0-9_]+)`?[[:space:]]+([a-zA-Z0-9_]+(?:\\(.*?\\))?)")
			colMatch := colRe.FindStringSubmatch(line)

			if len(colMatch) > 2 {
				columns = append(columns, Column{
					Name: colMatch[1],
					Type: colMatch[2],
				})
			}
		}

		tables = append(tables, Table{
			Name:    tableName,
			Columns: columns,
		})
	}
	return tables, nil
}

func generateGoCode(tables []Table) (string, error) {
	var builder strings.Builder
	builder.WriteString("// Code generated by 'go run generate.go'. DO NOT EDIT.\n\n")
	builder.WriteString("package types\n\n")

	needsTimeImport := false
	for _, table := range tables {
		for _, column := range table.Columns {
			if toGoType(column.Type) == "time.Time" {
				needsTimeImport = true
				break
			}
		}
		if needsTimeImport {
			break
		}
	}

	if needsTimeImport {
		builder.WriteString("import \"time\"\n\n")
	}

	for _, table := range tables {
		structName := toCamelCase(table.Name)
		builder.WriteString(fmt.Sprintf("type %s struct {\n", structName))
		for _, column := range table.Columns {
			fieldName := toCamelCase(column.Name)
			fieldType := toGoType(column.Type)
			builder.WriteString(fmt.Sprintf("\t%s %s `db:\"%s\"`\n", fieldName, fieldType, column.Name))
		}
		builder.WriteString("}\n\n")
	}

	builder.WriteString("type ModelDump struct {\n")
	for _, table := range tables {
		structName := toCamelCase(table.Name)
		builder.WriteString(fmt.Sprintf("\t%s []%s\n", structName, structName))
	}
	builder.WriteString("}\n")

	return builder.String(), nil
}

func generateStateCode(tables []Table) error {
	tmpl, err := template.ParseFiles("statement.tmpl")
	if err != nil {
		return err
	}

	f, err := os.Create("state/statement.go")
	if err != nil {
		return err
	}
	defer f.Close()

	var templateData []TemplateData
	for _, table := range tables {
		templateData = append(templateData, TemplateData{
			StructName: toCamelCase(table.Name),
			TableName:  table.Name,
		})
	}

	return tmpl.Execute(f, templateData)
}

func toGoType(sqlType string) string {
	sqlType = strings.ToUpper(sqlType)
	if strings.Contains(sqlType, "INT") {
		return "int"
	}
	switch sqlType {
	case "TEXT", "VARCHAR", "CHAR":
		return "string"
	case "FLOAT", "DOUBLE", "DECIMAL":
		return "float64"
	case "BOOLEAN":
		return "bool"
	case "DATE", "DATETIME", "TIMESTAMP":
		return "time.Time"
	default:
		return "interface{}"
	}
}

func toCamelCase(s string) string {
	parts := strings.Split(s, "_")
	for i, part := range parts {
		parts[i] = strings.Title(part)
	}
	return strings.Join(parts, "")
}
