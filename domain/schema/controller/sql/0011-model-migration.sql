-- model_migration is the canonical record of a single migration attempt for
-- a model. The uuid is generated by the source side and propagated to the
-- target with the export payload, so a single logical migration is
-- correlated by uuid across both controllers (one row per side).
CREATE TABLE model_migration (
    uuid TEXT NOT NULL PRIMARY KEY,
    model_uuid TEXT NOT NULL,
    direction_id INT NOT NULL,
    attempt INT NOT NULL,
    start_time TIMESTAMP NOT NULL,
    CONSTRAINT fk_model_migration_model
    FOREIGN KEY (model_uuid)
    REFERENCES model (uuid),
    CONSTRAINT fk_model_migration_direction
    FOREIGN KEY (direction_id)
    REFERENCES model_migration_direction (id)
);

CREATE UNIQUE INDEX idx_model_migration_attempt
ON model_migration (model_uuid, direction_id, attempt);

-- model_migration_status is the time-ordered history of (phase, message)
-- snapshots for a migration. A row is inserted on every phase transition
-- and on every status-message update. The current status of a migration is
-- the row with the maximum changed_at for that migration_uuid.
CREATE TABLE model_migration_status (
    uuid TEXT NOT NULL PRIMARY KEY,
    migration_uuid TEXT NOT NULL,
    phase_id INT NOT NULL,
    message TEXT,
    changed_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_model_migration_status_migration
    FOREIGN KEY (migration_uuid)
    REFERENCES model_migration (uuid),
    CONSTRAINT fk_model_migration_status_phase
    FOREIGN KEY (phase_id)
    REFERENCES model_migration_phase (id)
);

CREATE INDEX idx_model_migration_status_migration_changed_at
ON model_migration_status (migration_uuid, changed_at);

-- model_migration_target links an export-direction migration to the
-- external_controller it is being migrated to. Only present for export
-- rows; the import side does not have a target. Enforcement that the
-- linked migration has direction_id = 'export' is left to the service
-- layer.
CREATE TABLE model_migration_target (
    migration_uuid TEXT NOT NULL PRIMARY KEY,
    external_controller_uuid TEXT NOT NULL,
    CONSTRAINT fk_model_migration_target_migration
    FOREIGN KEY (migration_uuid)
    REFERENCES model_migration (uuid),
    CONSTRAINT fk_model_migration_target_external_controller
    FOREIGN KEY (external_controller_uuid)
    REFERENCES external_controller (uuid)
);

CREATE INDEX idx_model_migration_target_external_controller
ON model_migration_target (external_controller_uuid);
