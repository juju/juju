-- Patch 0031: Redesign the export-side model migration tables.
--
-- The flat model_migration table introduced in patch 0011 mixed target
-- credentials, phase tracking and status into a single row, and was paired
-- with model_migration_status / model_migration_user / model_migration_minion_sync
-- siblings whose relationships were not enforced.
--
-- The export and import sides of a model migration are deeply asymmetric. The
-- target's view of an in-progress import is already captured by
-- model_migration_import (patch 0027). This patch extends that target-side
-- import claim and normalises the source side into:
--
--   model_migration_phase                -- lookup of export-side phases
--   model_migration_export               -- one row per export attempt
--   model_migration_export_target_auth   -- per-migration target controller creds
--   model_migration_export_phase         -- phase entry history (1 per phase)
--   model_migration_export_status        -- current status message
--   model_migration_export_minion_sync   -- minion phase reports
--   model_migration_export_offer         -- hosted offer UUIDs captured
--   model_migration_redirect             -- durable post-REAP redirect snapshot
--   model_migration_redirect_user        -- captured model user access
--
-- The same migration uuid is generated by the source and propagated to the
-- target with the export payload, so both sides can be correlated.

-- Drop all legacy objects upfront: the changestream triggers first, then the
-- legacy tables they were attached to, followed by the model state view and the
-- import-claim index that are both recreated further down.
DROP TRIGGER trg_log_model_migration_status_insert;
DROP TRIGGER trg_log_model_migration_status_update;
DROP TRIGGER trg_log_model_migration_status_delete;
DROP TRIGGER trg_log_model_migration_minion_sync_insert;
DROP TRIGGER trg_log_model_migration_minion_sync_update;
DROP TRIGGER trg_log_model_migration_minion_sync_delete;

DROP TABLE model_migration_minion_sync;
DROP TABLE model_migration_user;
DROP TABLE model_migration_status;
DROP TABLE model_migration;

DROP VIEW v_model_state;

DROP INDEX idx_model_migration_import;

-- Extend the target-side import claim from patch 0027 without rewriting that
-- already-released patch. The existing table is renamed so its rows (old
-- in-flight imports) can be copied into the extended table below; their import
-- UUID is copied into source_migration_uuid so the new NOT NULL diagnostic
-- column is non-empty after upgrade. model_migration_import_old is dropped once
-- the copy completes -- it is the one drop that cannot be hoisted upfront.
ALTER TABLE model_migration_import RENAME TO model_migration_import_old;

