// GetByCIDR returns all subnets in the collection whose CIDR matches the
// input. It first attempts an exact string match; if none are found, it falls
// back to bidirectional CIDR containment (the target network is contained
// within a subnet's network, or vice-versa).
func (s SubnetInfos) GetByCIDR(cidr string) (SubnetInfos, error) {
	if !network.IsValidCIDR(cidr) {
		return nil, errors.Errorf("CIDR %q %w", cidr, coreerrors.NotValid)
	}

	var matching SubnetInfos
	for _, sub := range s {
		if sub.CIDR == cidr {
			matching = append(matching, sub)
		}
	}

	if len(matching) != 0 {
		return matching, nil
	}

	// Some providers carve subnets into smaller CIDRs and assign addresses
	// from the carved subnets to the machines. If we were not able to find a
	// direct CIDR match, fall back to bidirectional containment.
	_, targetNet, err := net.ParseCIDR(cidr)
	if err != nil {
		return nil, errors.Errorf("parsing CIDR %q: %w", cidr, err)
	}
	for _, sub := range s {
		snNet, err := sub.ParsedCIDRNetwork()
		if err != nil {
			continue
		}
		if targetNet.Contains(snNet.IP) || snNet.Contains(targetNet.IP) {
			matching = append(matching, sub)
		}
	}
	return matching, nil
}

// ParsedCIDRNetwork returns the network represented by the CIDR field.
func (s *SubnetInfo) ParsedCIDRNetwork() (*net.IPNet, error) {
	_, ipNet, err := net.ParseCIDR(s.CIDR)
	if err != nil {
		return nil, err
	}
	return ipNet, nil
}

// ImportSubnetArgs is the input for importing a single subnet as part of model
// migration. The caller must supply a UUID so the source-model identity is
